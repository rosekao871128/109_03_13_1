public class Hero {
    
    private String name , height , weight,gender,age;

    public void setName(String setname){
        this.name = setname;
    }

    public String getName(){
        return name;
    }

    public void setHeight(String setheight){
        this.height = setheight;
    }

    public String getHeight(){
        return height;
    }

    public void setWeight(String setweight){
        this.weight = setweight;
    }

    public String getWeight(){
        return weight;
    }

    public void setGender(String setgender){
        this.gender = setgender;
    }

    public String getGender(){
        return gender;
    }

    public void setAge(String setage){
        this.age = setage;
    }

    public String getAge(){
        return age;
    }
}